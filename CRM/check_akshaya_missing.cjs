function parseUserDate(str) {
    let clean = str.replace(/(_\d+)|(-\d+)$/, '').trim(); // Remove suffix
    const match = clean.match(/^(\d{1,2})[\s\-\/\.]+(\d{1,2})[\s\-\/\.]+(\d{2,4})/);
    if (match) {
        let [_, d, m, y] = match;
        if (y.length === 2) y = '20' + y;
        return `${y}-${m.padStart(2, '0')}-${d.padStart(2, '0')}`;
    }
    return null;
}

async function checkMissing() {
    const result = { missing: [], extra: [], found2023: false };

    const userItems = userDatesRaw.split(',').map(s => s.trim()).filter(s => s !== '' && s !== 'Orignal Dates');
    const userCounts = {};
    userItems.forEach(raw => {
        const parsed = parseUserDate(raw);
        if (parsed) {
            userCounts[parsed] = (userCounts[parsed] || 0) + 1;
        }
    });

    const { data: dbDeliveries } = await supabase
        .from('deliveries')
        .select('date')
        .ilike('delivery_name', '%Akshaya%');

    const dbCounts = {};
    dbDeliveries.forEach(d => {
        dbCounts[d.date] = (dbCounts[d.date] || 0) + 1;
    });

    for (const [date, count] of Object.entries(userCounts)) {
        const dbCount = dbCounts[date] || 0;
        if (dbCount < count) {
            result.missing.push({ date, diff: count - dbCount });
        }
    }

    // Check outlier
    const outlier = dbDeliveries.find(d => d.date === '2023-01-14');
    if (outlier) {
        result.found2023 = true;
    }

    fs.writeFileSync('akshaya_result.json', JSON.stringify(result, null, 2));
}

checkMissing();
